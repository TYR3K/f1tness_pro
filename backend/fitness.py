"""
Расчёт расхода калорий на тренировках.

Чистые функции без побочных эффектов: используются для оценки
сожжённых калорий по типу активности, длительности и весу пользователя.
Основано на MET (Metabolic Equivalent of Task) — стандартных коэффициентах
интенсивности физической нагрузки.
"""

from __future__ import annotations

# Таблица MET-коэффициентов по типам тренировок.
# MET — во сколько раз расход энергии при нагрузке выше, чем в покое.
MET_TABLE: dict[str, float] = {
    "cardio": 7.0,     # кардио (бег, велотренажёр и т.п.)
    "strength": 5.0,   # силовая тренировка
    "walking": 3.5,    # ходьба
    "yoga": 2.5,       # йога / растяжка
    "other": 4.0,      # прочая активность (значение по умолчанию)
}


def estimate_calories_burned(
    workout_type: str,
    duration_min: int,
    weight_kg: float | None,
) -> tuple[int, float]:
    """
    Оценить количество сожжённых калорий за тренировку.

    Формула: kcal = MET * вес(кг) * (длительность_мин / 60).

    Аргументы:
        workout_type — тип тренировки (ключ из MET_TABLE; неизвестный -> "other").
        duration_min — длительность тренировки в минутах.
        weight_kg    — вес пользователя в кг (если None — берём 70 кг по умолчанию).

    Возвращает:
        Кортеж (calories_burned, met):
          - calories_burned — округлённые сожжённые калории (не меньше 0);
          - met             — использованный MET-коэффициент.
    """
    # Берём MET для типа тренировки; для неизвестного типа — значение "other" (4.0).
    met = MET_TABLE.get(workout_type, 4.0)

    # Если вес не задан — используем усреднённое значение 70 кг.
    w = weight_kg or 70.0

    # Основная формула расхода энергии.
    kcal = met * w * (duration_min / 60.0)

    # Калории не могут быть отрицательными; округляем до целого.
    return (max(0, round(kcal)), met)
